import UIKit

extension UIColor {
    
    ///
    /// This will return the primary color of Apps
    ///
    /// - returns: the primary color of Apps
    ///
    class func PrimaryColor() -> UIColor {
        return UIColor(red: 13.0/255.0, green: 169.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
}

